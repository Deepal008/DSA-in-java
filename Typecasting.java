

public class Typecasting {
    public static void main(String[] args) {
        float a = 12.6f;   /*type casting is a way to convert data type of the variable from one to another. here some data can be loss . */
        int b = (int)a;
        System.out.println(b);
        char d = 'a';
        int c = (int)d;
        System.out.println(c);
    }
}
